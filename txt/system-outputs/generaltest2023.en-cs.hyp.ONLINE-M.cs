Navíc, odvolání Nejvyššího soudu proti pokusu tehdejšího prezidenta Richarda Nixona vyhnout se předvolání ukazuje, že vágní tvrzení o privilegiích nebude stačit a může být překonáno specifickou potřebou důkazů.
Soud pozoroval v roce 1974 Spojené státy v. Nixonová:
... je-li důvod pro uplatnění výsady ohledně předvolaných materiálů, které mají být použity v trestním řízení, založen pouze na všeobecném zájmu o důvěrnost, nemůže převážit nad základními požadavky řádného soudního řízení při spravedlivém výkonu trestního soudnictví.
Obecné uplatňování výsady musí vyhovět prokázané, specifické potřebě důkazů v probíhajícím trestním řízení.
Proto je precedent na straně vlády.
